/* 运行时用（支持 0x3C/0x3D 两种模块） */
static uint16_t s_oled_addr = OLED_ADDRESS;

static HAL_StatusTypeDef OLED_I2C_Send(const uint8_t *buf, uint16_t len)
{
    return HAL_I2C_Master_Transmit(&OLED_I2C_HANDLE, (uint16_t)s_oled_addr, (uint8_t *)buf, len, 1000U);
}

static void OLED_DetectAddress(void)
{
    if (HAL_I2C_IsDeviceReady(&OLED_I2C_HANDLE, OLED_ADDRESS, 3U, 100U) == HAL_OK) {
        s_oled_addr = OLED_ADDRESS;
        return;
    }
    if (HAL_I2C_IsDeviceReady(&OLED_I2C_HANDLE, 0x7AU, 3U, 100U) == HAL_OK) {
        s_oled_addr = 0x7AU;
        return;
    }
    s_oled_addr = OLED_ADDRESS;
}

static void OLED_WriteData(uint8_t data)
{
    uint8_t buf[2] = {OLED_DATA, data};
    (void)OLED_I2C_Send(buf, 2U);
}

/* 页地址模式下列会自动 +1，可一次发送多列数据（减轻 I2C 负载） */
static void OLED_WriteDataBurst(const uint8_t *data, uint16_t len)
{
    uint8_t tmp[129];
    uint16_t chunk;
    uint16_t pos = 0U;
    while (pos < len) {
        chunk = len - pos;
        if (chunk > 128U) {
            chunk = 128U;
        }
        tmp[0] = OLED_DATA;
        memcpy(tmp + 1U, data + pos, chunk);
        (void)OLED_I2C_Send(tmp, (uint16_t)(chunk + 1U));
        pos += chunk;
    }
}

void OLED_WR_Byte(uint8_t dat, uint8_t cmd)
{
    uint8_t buf[2] = {cmd, dat};
    (void)OLED_I2C_Send(buf, 2U);
}

void OLED_Set_Pos(uint8_t x, uint8_t y)
{
    uint16_t px = (uint16_t)x + (uint16_t)OLED_COLUMN_OFFSET;
    if (px > 127U) {
        px = 127U;
    }
    OLED_WR_Byte((uint8_t)(0xB0U + y), OLED_CMD);
    OLED_WR_Byte((uint8_t)(((px & 0xF0U) >> 4) | 0x10U), OLED_CMD);
    OLED_WR_Byte((uint8_t)(px & 0x0FU), OLED_CMD);
}

